#include "../cub3d.h"

void	get_door_total(char **map, t_data *data)
{
	int	x;
	int	y;

	x = 0;
	while (map[x])
	{
		y = 0;
		while (map[x][y])
		{
			if (map[x][y] == 'D')
				data->doors.total++;
			y++;
		}
		x++;
	}
}

